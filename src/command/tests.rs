#[test]
fn response_can_accumulate_followup_command_effects() {
    let window = window::Id::new(1);
    let context = Context::window(window);
    let call = Call::<CustomFlag>::for_context::<FlagTarget>(true, context.clone())
        .expect("bool args should validate");
    let response = Response::output("done".to_owned()).then_call(call);

    assert_eq!(response.effects().len(), 1);

    let (output, effects) = response.into_parts();

    assert_eq!(output, "done");
    let [Effect::Call(call)] = effects.as_slice() else {
        panic!("expected one follow-up call effect");
    };
    assert!(call.is::<CustomFlag>());
    assert_eq!(call.context(), Some(context));
}

