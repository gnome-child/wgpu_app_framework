#[test]
fn registry_rejects_duplicate_commands_and_shortcuts_atomically() {
    let mut registry = Registry::new();
    let shortcut = Shortcut::ctrl('a');

    registry.define::<SelectAll, TestTarget>(|command| command.shortcut(shortcut));

    assert_eq!(
        registry.try_define::<SelectAll, TestTarget>(|command| command),
        Err(RegisterError::DuplicateCommand {
            command: SelectAll::NAME,
        })
    );
    assert_eq!(
        registry.try_define::<Copy, TestTarget>(|command| command.shortcut(shortcut)),
        Err(RegisterError::DuplicateShortcut {
            shortcut,
            existing: SelectAll::NAME,
            duplicate: Copy::NAME,
        })
    );
    assert!(registry.command_key(COPY).is_none());
    assert_eq!(
        registry
            .shortcut_command(shortcut)
            .map(|route| route.command()),
        Some(SELECT_ALL)
    );
}

#[test]
fn registry_accepts_closure_command_batches() {
    let mut registry = Registry::new();

    registry.commands(|commands| {
        commands
            .define::<SelectAll, TestTarget>(|command| command.shortcut(Shortcut::ctrl('a')))
            .define::<Copy, TestTarget>(|command| command.shortcut(Shortcut::ctrl('c')));
    });

    assert_eq!(
        registry
            .shortcut_command(Shortcut::ctrl('a'))
            .map(|route| route.command()),
        Some(SELECT_ALL)
    );
    assert_eq!(
        registry
            .shortcut_command(Shortcut::ctrl('c'))
            .map(|route| route.command()),
        Some(COPY)
    );
}

