const TEXT_BOX: ui::Id = ui::Id::new("text_box");

struct CustomFlag;
struct SaveAs;
trait MacroTarget: 'static {}

impl target::Category for dyn MacroTarget {}

crate::command!(MacroGenerated {
    name: "macro_generated",
    display: "Macro Generated",
    args: String,
    output: bool,
    hint: "Generated by command macro",
    repeatable: true,
    target: dyn MacroTarget,
});

impl Command for CustomFlag {
    type Args = bool;
    type Output = ();

    const NAME: &'static str = "custom_flag";
    const DISPLAY: &'static str = "Custom Flag";
}

impl Command for SaveAs {
    type Args = PathBuf;
    type Output = ();

    const NAME: &'static str = "save_as";
    const DISPLAY: &'static str = "Save As";
}

const SELECT_ALL: Key = Key::of::<SelectAll>();
const COPY: Key = Key::of::<Copy>();
const INSERT_TEXT: Key = Key::of::<InsertText>();
const CUSTOM_FLAG: Key = Key::of::<CustomFlag>();

struct FlagTarget {
    value: bool,
}

impl Target<CustomFlag> for FlagTarget {
    fn state(&self, _context: &Context) -> State {
        State::active_if(self.value)
    }

    fn invoke(&mut self, args: bool, _invocation: Invocation<CustomFlag>) -> Response<()> {
        self.value = args;
        Response::none()
    }
}

